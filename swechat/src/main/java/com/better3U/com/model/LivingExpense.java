package com.better3U.com.model;

import java.util.Date;

/**
 * @TODO 水电气花费对应的实体类
 * @author tangyuan
 * @date 2018/3/13 14:46
 **/
public class LivingExpense {
    private int category;
    private double spendPerMonth;
    private double leftMoney;
    private double depositMoney;
    private Date addTime;
    private Date seeTime;

    public int getCategory() {
        return category;
    }

    public void setCategory(int category) {
        this.category = category;
    }

    public double getSpendPerMonth() {
        return spendPerMonth;
    }

    public void setSpendPerMonth(double spendPerMonth) {
        this.spendPerMonth = spendPerMonth;
    }

    public double getLeftMoney() {
        return leftMoney;
    }

    public void setLeftMoney(double leftMoney) {
        this.leftMoney = leftMoney;
    }

    public double getDepositMoney() {
        return depositMoney;
    }

    public void setDepositMoney(double depositMoney) {
        this.depositMoney = depositMoney;
    }

    public Date getAddTime() {
        return addTime;
    }

    public void setAddTime(Date addTime) {
        this.addTime = addTime;
    }

    public Date getSeeTime() {
        return seeTime;
    }

    public void setSeeTime(Date seeTime) {
        this.seeTime = seeTime;
    }
}
